package ReviewClass3;

public class ForLoopPractice {

	public static void main(String[] args) {
    
		for(int i=1; i<=5; i++) {
		    System.out.println(" Hello "+" ");
		}
		System.out.println();
		for(int r =10; r>=1; r--) {
			System.out.print(r+" ");
		}
		System.out.println();

		for(int m=1;m<=20;m++) {
			if(m%2!=0) {			
				System.out.print(m+" ");
}
		}
	}

}
