package Class7;

public class Practice1farLoop {
public static void main(String []args ) {
	
 	for(int k =1;k<=100;k++) {
 		System.out.print(k+" ");
 		
 	}
 	System.out.println("++++++++++");
	System.out.println();

 	for (int m =100;m>=1;m--) {
			System.out.print(m+" ");
		}
 	System.out.println();
 	
 	
 	for(int b=20;b>1;b-=2) {
 		System.out.print(b+" ");
 	}
 	System.out.println();

 	
 	for(int o =40;o>1;o--) {
 		if(o%2==0) {
 			System.out.print(o+" ");
 		}
 	}
 	System.out.println();

 	for (int p = 20;p>1;p--) {
 		if(p%2==1) {
 			System.out.print(p+" ");
 		}
 	}
}
}
